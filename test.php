                	<h1 class="title">About Bookstore Genie</h1>
                    
                    <p>When was the last time you saw a "Genie in the Bottle" dispense a real-life miracle? Well, given these uncertain economic times, he's come out and morphed into the Bookstore Genie. Rising from a stack of once expensive textbooks, he's now younger, chubbier and some even say "cuter." You can judge that one for yourself.</p>

<p>The Bookstore Genie's new life mission is to save college students tons of money on textbooks. The Bookstore Genie recalls when he was a student at The Genie Academy, where textbooks would cost less than half of what they sell for at most college campuses today. He doesn't understand why the cost of a regular textbook has risen more than 12 times the rate of inflation. The Genie thinks overpriced books are unfair, and being that he's a Genie, he set out to do something about it.</p>

<p>Bookstore Genie is reinventing the formula and relationship students have with their textbooks... where students have become accustomed to having a love-hate relationship with their books, they now have a little more love and a lot less hate. Students can now <strong>RENT</strong> their books from Bookstore Genie for a fraction of what it would cost to rent or buy them from a <strong>Non-Genie Entity</strong> (NGE... basically everybody else). </p>

<p>Bookstore Genie was founded by Farhan Daredia, a bright and charismatic student at George Washington University. After winning second place in the GW Business Plan Competition, Farhan is now extending the reach of the Genie's magic throughout the country. Students at college campuses across the country are signing up as Genie Reps to spread the good word by inviting students to rent their books from Bookstore Genie. Thanks to the Genie, everyone is saving money -- and there is definitely some magic in that!</p>

<p>Believe it or not, the Bookstore Genie team does not operate inside a bottle. We work out of an office building just a few blocks away from our humble college beginnings. We balance hard work with fun, but are all driven by one basic belief: <strong>all students deserve a Genie they can trust!</strong></p>

